# What is Slicing on List

# Deine a name of list
a = ["Mona","Ruhi","Aman","Vinesh","Geeta","Smriti"]

# Prints elements from index -6 to -3 (inclusive of -6, exclusive of -3)
print(a[-6:-3])
# Print elements from index 1 to 4 (inclusive of 1, exclusive of 4)
print(a[1:4])
# Print all elements from starting index to ending index
print(a[::])
# Print elements from the start to index 3 (exclusive of 3)
print(a[:3])
# Print elements from index 2 to the end of the list
print(a[2:])
# Print every second element from the list
print(a[::2])
# Print elements from the list in reverse order
print(a[::-1])